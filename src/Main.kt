fun main() {
	val name = "Fortune"
	println(name)
	var a = 10
	var b = 3
	println(a + b)
	println(a - b)
	println(a * b)
	println(a / b)
	println(a % b)

	println(a > b)
	println(a < b)

	var increment = a++
	var decrement = b--
	println(increment)
	println(decrement)


	println(++a)
	println(a)

}
